#1. Web scraping is usually performed with HTML and is less structured. Web API is associated with XML and JSON and is more structured.

#2. Web crawling is ubiquitous in the sense that a large portion of web pages are indiscriminately indexed while web scraping is a more active pull of queries from web pages.

#3. The robots.txt file issues instructions about the site to web robots (disallowing them from visiting its pages).

#4. R is adequate for scraping web pages but may be edged by Python.
